                <p>Chris started roofing in Edmonton at the age of sixteen during summer school breaks. Once high school was finished he started his 3 year roofing contractors apprenticeship.</p>
                <p>He now shares a unique Canadian experience: The Edmonton roof repair company that he apprenticed under was multi-cultured.  The Foremen were: an Italian (WWII Vet), a Ukrainian (WWII Vet), a German (WWII Vet), a French Canadian, and a Native American.</p>
                <p>There was a recession in 1981 &amp; 82 when Chris started roofing repairs. The competition was fierce between foremen, as one’s job seemed to always be on the line. If a worker/roofer did not cut it, quite simply he was gone. All employers demanded absolute perfection, which Chris was proud to have gone through.  It has made him who he is today and what he expects from his men, to run a successful Edmonton roofing company.</p>
                <p>He gained all his experience from large established Edmonton Roofing Contractors, and it wasn’t uncommon for a roofing inspector to be looking over workers’ shoulders most of the time, so doing excellent work became habit.</p>
